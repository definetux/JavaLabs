    public BigNumbers(boolean decorator) {
        if (decorator == true) {
            for (int i = 0; i < NUMBER_SIDE; i++) {
                for (int k = 0; k < _numbers.length; k++) {
                    for (int j = 0; j < NUMBER_SIDE; j++) {
                        if (_numbers[k][i][j] == '*') {
                            _numbers[k][i][j] = Character.forDigit(k, 10);
                        }
                    }
                }
            }
        }
    }

